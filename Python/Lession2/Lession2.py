#print arithmetic operations of 2 numbers

num1 = 3
num2 = 20

# Arithmetic operators
print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num2/num1)
print(num1//num2)
print(num2 % num1)
print(num2 ** num1)

# Relational operators
print(num1 == num2)
print(num1 != num2)
print(num1 >= num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 < num2)

#Assignment operators
num = 10
num **= 5
print('num:', num)

#Logical not operators
print(not False)
print(not True)
print(not (num2 > num1))
print(not (num1 > num2))

# and operator
val1 = True
val2 = True
print('and operator answer is: ', val1 and val2)
val2 = False
print('and operator answer is: ', val1 and val2)
val1 = False
val2 = True
print('and operator answer is: ', val1 and val2)
val1 = False
val2 = False
print('and operator answer is: ', val1 and val2)

#or operator
val1 = True
val2 = False
print("or operator answer: ", val1 or val2)
val2 = True
print("or operator answer: ", val1 or val2)
val1 = False
print("or operator answer: ", val1 or val2)
val2 = False

# Convert string to integer
num_str = "10"
num_int = int(num_str)
print(type(num_int))  # Output: <class 'int'>
print("string convert to integer:", num_int)  # Output: 10

# Convert integer to string
num = 25
num_str = str(num)
print(type(num_str))  # Output: <class 'str'>
print("integer convert to string:", num_str)  # Output: "25"

# Convert float to integer
pi = 3.14
pi_int = int(pi)
print(type(pi_int))  # Output: <class 'int'>
print("float convert to integer:", pi_int)  # Output: 3